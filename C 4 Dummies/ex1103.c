#include<stdio.h>

int main(void)
{
    int i;
    i = -5;
    while (i <= 5)
    {
        printf("%d ", i);
        i++;
    }
    while (i >= -5)
    {
        printf("%d ", i);
        i--;
    }
    putchar('\n');
    return (0);
}
